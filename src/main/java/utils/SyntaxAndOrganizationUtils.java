package utils;

public class SyntaxAndOrganizationUtils {

    public static void title(String title){
        System.out.println("");
        System.out.println("@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@  "+title.toUpperCase()+"  @@@@@@@@@@@@@@@@@@@@@@@@@@@@@@");
        System.out.println("");
    }
    public static void startOrEnd(String title){
        System.out.println("");
        System.out.println("************************************************************    "+title.toUpperCase()+"    ************************************************************");
        System.out.println("");
    }
}
